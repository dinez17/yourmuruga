function setLanguage(language) {
  document.documentElement.lang = language === "ta" ? "ta" : "en";
  document.body.classList.toggle("lang-ta", language === "ta");

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[language][key] !== undefined) {
      node.textContent = translations[language][key];
    }
  });

  languageContentNodes.forEach((node) => {
    node.hidden = node.dataset.contentLang !== language;
  });

  localizedBlocks.forEach((block) => {
    const node = document.querySelector(block.selector);
    if (node) {
      node.innerHTML = language === "en" ? block.en : block.ta;
    }
  });

  bindAllAccordions();
  bindMiracleTabs();

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("yourMurugaLanguage", language);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.lang);
    setLanguagePickerOpen(false);
    setMenuOpen(false);
  });
});

