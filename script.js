function showMuruganMessage(selectedNumber, errorNode, sourceInput, sourceForm) {
  if (!Number.isInteger(selectedNumber) || selectedNumber < 1 || selectedNumber > 100) {
    if (errorNode) {
      errorNode.textContent = document.documentElement.lang === "ta"
        ? "தயவுசெய்து 1 முதல் 100 வரை ஒரு எண்ணை உள்ளிடுங்கள்."
        : "Please enter a number from 1 to 100.";
    }

    if (muruganMessage) {
      muruganMessage.textContent = "";
    }

    return;
  }

  if (messageError) {
    messageError.textContent = "";
  }

  if (mainMessageError) {
    mainMessageError.textContent = "";
  }

  if (muruganMessage) {
    muruganMessage.textContent = getMuruganMessage(selectedNumber);
  }

  sourceForm?.classList.add("is-loading");
  sourceInput?.setAttribute("disabled", "true");
  closeDevotionPopup();
  openLoadingPopup();

  window.setTimeout(() => {
    sourceInput?.removeAttribute("disabled");
    sourceForm?.classList.remove("is-loading");
    closeLoadingPopup();
    openMessagePopup();
  }, 2000);
}

